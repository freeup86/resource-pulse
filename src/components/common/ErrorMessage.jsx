import React, { useState } from 'react';

const ErrorMessage = ({ message, onDismiss = null }) => {
  const [dismissed, setDismissed] = useState(false);

  if (dismissed) {
    return null;
  }

  const handleDismiss = () => {
    setDismissed(true);
    if (onDismiss) {
      onDismiss();
    }
  };

  return (
    <div className="bg-red-100 border border-red-400 text-red-700 px-4 py-3 rounded relative mb-4" role="alert">
      <strong className="font-bold">Error: </strong>
      <span className="block sm:inline">{message || 'Something went wrong.'}</span>
      <button
        onClick={handleDismiss}
        className="absolute top-0 right-0 mt-3 mr-4 text-red-700 hover:text-red-900"
        aria-label="Dismiss"
      >
        <svg className="h-4 w-4 fill-current" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20">
          <path d="M10 8.586L3.707 2.293 2.293 3.707 8.586 10l-6.293 6.293 1.414 1.414L10 11.414l6.293 6.293 1.414-1.414L11.414 10l6.293-6.293-1.414-1.414L10 8.586z" />
        </svg>
      </button>
    </div>
  );
};

export default ErrorMessage;